# 如何用列表来表示全班同学的姓名？
students = ['小明', '小红', '小强']

# 如何获取列表中第三个同学的姓名？
print(students[2])

# 如何获取列表中倒数第二个同学的姓名？
print(students[-2])

# 如何在列表的末尾添加一个同学'小白'？
students.append('小白')
print(students)

# 如何删除列表末尾这个同学的姓名？
students.pop()
print(students)

# 如何在列表的第二个位置添加'小白'？
students.insert(1, '小白')
print(students)

# 如何删除列表中第二个同学的姓名？
students.pop(1)
print(students)




